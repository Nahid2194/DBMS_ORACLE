DROP TABLE BONUS;
DROP TABLE TITLE;
DROP TABLE WORKER;
CREATE TABLE Worker (
	WORKER_ID NUMBER(5),
	FIRST_NAME VARCHAR(25),
	LAST_NAME VARCHAR(25),
	SALARY NUMBER(15),
	JOINING_DATE VARCHAR(25),
	DEPARTMENT VARCHAR(25),
	PRIMARY KEY(WORKER_ID)
);

CREATE TABLE Bonus (
	WORKER_REF_ID NUMBER(5),
	BONUS_AMOUNT NUMBER(10),
	BONUS_DATE VARCHAR(25),
	FOREIGN KEY (WORKER_REF_ID) REFERENCES Worker(WORKER_ID) ON DELETE CASCADE
);
CREATE TABLE Title (
	WORKER_RE_ID NUMBER(5),
	WORKER_TITLE VARCHAR(25),
	AFFECTED_FROM VARCHAR(25),
	FOREIGN KEY (WORKER_RE_ID) REFERENCES Worker(WORKER_ID) ON DELETE CASCADE
);



